char	*ft_strcat(char *s1, char const *s2)
{
	char	*ptr;

	ptr = s1;
	while (*ptr++)
		;
	ptr--;
	while (*s2)
		*ptr++ = *s2++;
	*ptr = '\0';
	return (s1);
}
